class forloop_demo {

    public static void main(String args[]) {
        for (int i = 1; i < 10; i++) {
            System.out.println("loop :" + i);
            if (i > 5) {
                System.out.println("i is big, where i = " + i);
            } else {
                System.out.println("i is small, where i = " + i);
            }

        }

    }

}